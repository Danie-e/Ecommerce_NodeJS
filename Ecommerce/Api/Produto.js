class Produto {
    constructor(Nome, Codigo, Preco, Descricao, Estoque, Avaliacao, Categoria, Imagem) {
        this.Nome = Nome,
            this.Codigo = Codigo,
            this.Preco = Preco,
            this.Descricao = Descricao,
            this.Estoque = Estoque,
            this.Avaliacao = Avaliacao,
            this.Categoria = Categoria,
            this.Imagem = Imagem
    }
}

module.exports = Produto